    public class AcString
    {
        public string String = "";
        private static SaveType _saveType;
        private static string[] _charDictionary;

        public AcString(byte[] stringBuffer, SaveType saveType)
        {
            _saveType = saveType;
            switch (saveType)
            {
                case SaveType.DoubutsuNoMori:
                case SaveType.DoubutsuNoMoriPlus:
                case SaveType.DoubutsuNoMoriEPlus:
                case SaveType.AnimalForestEPlus:
                    _charDictionary = CharacterSets.DoubutsuNoMoriEPlusCharMap;
                    break;
                case SaveType.AnimalCrossing:
                    _charDictionary = CharacterSets.AcCharacterDictionary;
                    break;
                case SaveType.WildWorld:
                    _charDictionary = CharacterSets.WwCharacterDictionary;
                    break;
                default:
                    _charDictionary = null;
                    break;
            }

            if (_charDictionary != null)
            {
                foreach (var character in stringBuffer)
                {
                    String += _charDictionary[character];
                }
            }
            else
            {
                switch (saveType)
                {
                    case SaveType.DongwuSenlin:
                        for (var c = 0; c < stringBuffer.Length; c++)
                        {
                            var result = GetCharacterIndex(stringBuffer, ref c);

                            if (result >= CharacterSets.DongwuSenlinCharacterSet.Length)
                            {
                                String += "?";
                            }
                            else
                            {
                                String += CharacterSets.DongwuSenlinCharacterSet[result];
                            }
                        }

                        break;
                    case SaveType.CityFolk:
                        String = Encoding.BigEndianUnicode.GetString(stringBuffer);
                        break;
                    case SaveType.NewLeaf:
                    case SaveType.WelcomeAmiibo:
                        String = Encoding.Unicode.GetString(stringBuffer);
                        break;
                    default:
                        String = "";
                        break;
                }
            }
        }

        public static byte[] GetBytes(string String, int maxSize = 0)
        {
            var i = 0;
            switch (_saveType)
            {
                case SaveType.AnimalCrossing:
                    var returnedString = new byte[maxSize > 0 ? maxSize : String.Length];
                    var t = StringInfo.GetTextElementEnumerator(String);
                    while (t.MoveNext() && i < returnedString.Length)
                    {
                        var idx = Array.IndexOf(_charDictionary, (string) t.Current);
                        if (idx > -1)
                            returnedString[i] = (byte) idx;
                        else
                            returnedString[i] = Encoding.UTF8.GetBytes(new[] { String[t.ElementIndex] })[0];
                        i++;
                    }
                    for (var x = 0; x < returnedString.Length; x++)
                        if (returnedString[x] == 0)
                            returnedString[x] = 0x20;
                    return returnedString;
                case SaveType.DoubutsuNoMori:
                case SaveType.DoubutsuNoMoriPlus:
                case SaveType.DoubutsuNoMoriEPlus:
                case SaveType.AnimalForestEPlus:
                    {
                    var stringBytes = new byte[maxSize > 0 ? maxSize : String.Length];
                    for (i = 0; i < String.Length; i++)
                    {
                        if (i >= stringBytes.Length)
                            break;

                        var idx = Array.IndexOf(CharacterSets.DoubutsuNoMoriEPlusCharMap, String[i].ToString());
                        if (idx > -1)
                        {
                            stringBytes[i] = (byte) idx;
                        }
                        else
                        {
                            stringBytes[i] = Encoding.ASCII.GetBytes(new[] { String[i] })[0];
                        }
                    }

                    if (maxSize <= 0 || String.Length >= maxSize) return stringBytes;
                    for (i = String.Length; i < maxSize; i++)
                    {
                        stringBytes[i] = 0x20;
                    }
                    return stringBytes;
                }
                case SaveType.WildWorld:
                {
                    var stringBuffer = new byte[maxSize > 0 ? maxSize : String.Length];
                    for (i = 0; i < String.Length; i++)
                    {
                        var idx = Array.IndexOf(CharacterSets.WwCharacterDictionary, String[i].ToString());
                        if (idx > -1)
                        {
                            stringBuffer[i] = (byte) idx;
                        }
                        else
                        {
                            stringBuffer[i] = 0x85; // Space
                        }
                    }
                
                    return stringBuffer;
                }
                case SaveType.DongwuSenlin:
                {
                    var stringBuffer = new byte[maxSize > 0 ? maxSize : String.Length];

                    int x = 0;
                    for (i = 0; i < String.Length; i++, x++)
                    {
                        var idx = Array.IndexOf(CharacterSets.DongwuSenlinCharacterSet, String[i].ToString());
                        if (idx > -1)
                        {
                            var (multiByte, charIdx, bank) = EncodeCharacterFromIndex(idx);
                            if (multiByte)
                            {
                                if (i + 1 < stringBuffer.Length)
                                {
                                    stringBuffer[x++] = charIdx;
                                    stringBuffer[x] = bank;
                                }
                                else
                                {
                                    stringBuffer[x] = 0x20; // Space
                                }
                            }
                            else
                            {
                                stringBuffer[x] = charIdx;
                            }
                        }
                        else
                        {
                            stringBuffer[x] = 0x20; // Space
                        }
                    }

                    return stringBuffer;
                }
                case SaveType.CityFolk:
                {
                    var stringBuffer = new byte[maxSize > 0 ? maxSize : String.Length * 2]; //Characters are now unicode
                    var stringBytes = Encoding.Unicode.GetBytes(String);
                    for (i = 0; i < stringBytes.Length; i += 2)
                    {
                        stringBuffer[i] = stringBytes[i + 1];
                        stringBuffer[i + 1] = stringBytes[i];
                    }
                    return stringBuffer;
                }
                case SaveType.NewLeaf:
                case SaveType.WelcomeAmiibo:
                {
                    var stringBuffer = Encoding.Unicode.GetBytes(String);
                    if (maxSize > 0)
                        Array.Resize(ref stringBuffer, maxSize);
                    return stringBuffer;
                }
                default:
                    return null;
            }
        }

        public string Trim()
            => String.Trim(' ');

        public string Clean()
            => Regex.Replace(Regex.Replace(String, "[\n]{3,}", "\n\n").Trim(' '), "[ ]{2,}", " ");

        // Dongwu Senlin specific
        private static int GetCharacterIndex(in byte[] textData, ref int index)
        {
            if (index >= textData.Length)
            {
                return 0;
            }

            int characterIndex = textData[index];
            if (characterIndex <= 0x7F) return characterIndex;

            int bank = textData[index + 1];
            characterIndex = textData[index++] - 0x80 + (bank + 1) * 0x80;
            return characterIndex;

        }

        private static (bool, byte, byte) EncodeCharacterFromIndex(int characterIndex) => characterIndex < 0x80
            ? ((bool, byte, byte))(false, (byte)characterIndex, 0)
            : (true, (byte)((characterIndex & 0x7F) + 0x80), (byte)((characterIndex - 0x80) / 0x80));
    }
